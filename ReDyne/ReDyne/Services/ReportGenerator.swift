    }

    // MARK: - Section 8: ObjC Classes

    private static func objcClassesSection(output: DecompiledOutput) -> String {
        guard let objcResult = output.objcAnalysis as? ObjCAnalysisResult,
              !objcResult.classes.isEmpty else {
            return ""
        }

        let classes = objcResult.classes.sortedByName()

        var html = """
        <div class="section">
            <h2 class="section-title">Objective-C Classes (\(classes.count))</h2>
            <div class="table-container">
                <table>
                    <thead>
                        <tr>
                            <th>Class</th>
                            <th>Superclass</th>
                            <th>Methods</th>
                            <th>Properties</th>
                            <th>Protocols</th>
                        </tr>
                    </thead>
                    <tbody>
        """

        for (index, cls) in classes.enumerated() {
            let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
            let protocols = cls.protocols.isEmpty ? "-" : cls.protocols.joined(separator: ", ")
            html += """
                        <tr class="\(rowClass)">
                            <td><strong>\(escapeHTML(cls.name))</strong>\(cls.isSwift ? " <span class=\"swift-badge\">Swift</span>" : "")</td>
                            <td>\(escapeHTML(cls.superclassName.isEmpty ? "-" : cls.superclassName))</td>
                            <td>\(cls.totalMethods)</td>
                            <td>\(cls.properties.count)</td>
                            <td class="detail-text">\(escapeHTML(protocols))</td>
                        </tr>
            """
        }

        html += """
                    </tbody>
                </table>
            </div>
        </div>
        """
        return html
    }

    // MARK: - Section 9: Swift Types

    private static func swiftTypesSection(output: DecompiledOutput) -> String {
        guard let swiftMeta = output.swiftMetadata as? SwiftMetadataAnalysis,
              swiftMeta.hasSwiftMetadata,
              !swiftMeta.types.isEmpty else {
            return ""
        }

        let types = swiftMeta.types.sorted { $0.name < $1.name }

        var html = """
        <div class="section">
            <h2 class="section-title">Swift Types (\(types.count))</h2>
            <div class="table-container">
                <table>
                    <thead>
                        <tr>
                            <th>Name</th>
                            <th>Kind</th>
                            <th>Fields</th>
                            <th>Generic</th>
                            <th>VTable</th>
                        </tr>
                    </thead>
                    <tbody>
        """

        for (index, type) in types.enumerated() {
            let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
            html += """
                        <tr class="\(rowClass)">
                            <td><strong>\(escapeHTML(type.name))</strong></td>
                            <td><span class="kind-badge">\(escapeHTML(type.kindString))</span></td>
                            <td>\(type.fieldCount)</td>
                            <td>\(type.isGeneric ? "Yes" : "No")</td>
                            <td>\(type.hasVTable ? "Yes" : "No")</td>
                        </tr>
            """
        }

        html += """
                    </tbody>
                </table>
            </div>
        </div>
        """
        return html
    }

    // MARK: - HTML Helpers

    private static func infoItem(label: String, value: String) -> String {
        return """
            <div class="info-item">
                <div class="info-label">\(label)</div>
                <div class="info-value">\(value)</div>
            </div>
        """
    }

    private static func escapeHTML(_ string: String) -> String {
        return string
            .replacingOccurrences(of: "&", with: "&amp;")
            .replacingOccurrences(of: "<", with: "&lt;")
            .replacingOccurrences(of: ">", with: "&gt;")
            .replacingOccurrences(of: "\"", with: "&quot;")
    }

    private static func postureRatingColor(posture: SecurityPosture) -> String {
        if posture.criticalCount > 0 { return "#dc3545" }
        if posture.highCount > 0 { return "#e67e22" }
        if posture.mediumCount > 0 { return "#f39c12" }
        if posture.lowCount > 0 { return "#3498db" }
        return "#27ae60"
    }

    private static func statusCSSClass(for status: SecurityIndicatorStatus) -> String {
        switch status {
        case .present:       return "status-present"
        case .absent:        return "status-absent"
        case .uncertain:     return "status-uncertain"
        case .notApplicable: return "status-na"
        @unknown default:    return "status-uncertain"
        }
    }

    private static func severityCSSClass(for severity: SecurityFindingSeverity) -> String {
        switch severity {
        case .info:     return "sev-info"
        case .low:      return "sev-low"
        case .medium:   return "sev-medium"
        case .high:     return "sev-high"
        case .critical: return "sev-critical"
        @unknown default: return "sev-info"
        }
    }

