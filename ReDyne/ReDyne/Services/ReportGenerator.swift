        for (index, exp) in exports.enumerated() {
            let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
            html += """
                        <tr class="\(rowClass)">
                            <td><code>\(escapeHTML(exp.displayName))</code></td>
                            <td><code>\(Constants.formatAddress(exp.address))</code></td>
                        </tr>
