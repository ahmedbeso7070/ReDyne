import Foundation
import UIKit

/// Generates comprehensive, professional HTML analysis reports from DecompiledOutput.
/// Phase 5, item 29 / To-Do 17.4 - Analysis Report Generation.
class ReportGenerator {

    // MARK: - Public API

    /// Generate a complete HTML analysis report from the decompiled output.
    /// - Parameters:
    ///   - output: The full DecompiledOutput model.
    ///   - fileName: Display name of the binary file.
    /// - Returns: A complete HTML document string ready to be written to a file.
    static func generateHTML(from output: DecompiledOutput, fileName: String) -> String {
        var html = htmlHeader(fileName: fileName)
        html += reportHeader(fileName: fileName, output: output)
        html += openContent()
        html += binaryOverviewSection(output: output)
        html += securityAssessmentSection(output: output)
        html += statisticsSummarySection(output: output)
        html += segmentsAndSectionsSection(output: output)
        html += symbolTableSection(output: output)
        html += importsSection(output: output)
        html += exportsSection(output: output)
        html += objcClassesSection(output: output)
        html += swiftTypesSection(output: output)
        html += closeContent()
        html += reportFooter()
        html += htmlFooter()
        return html
    }

    // MARK: - HTML Structure

    private static func htmlHeader(fileName: String) -> String {
        return """
        <!DOCTYPE html>
        <html lang="en">
        <head>
            <meta charset="UTF-8">
            <meta name="viewport" content="width=device-width, initial-scale=1.0">
            <title>\(escapeHTML(fileName)) - ReDyne Analysis Report</title>
            <style>
                \(cssStyles())
            </style>
        </head>
        <body>
        <div class="report-container">
        """
    }

    private static func htmlFooter() -> String {
        return """
        </div>
        </body>
        </html>
        """
    }

    private static func openContent() -> String {
        return "<div class=\"content\">\n"
    }

    private static func closeContent() -> String {
        return "</div>\n"
    }

    // MARK: - Report Header

    private static func reportHeader(fileName: String, output: DecompiledOutput) -> String {
        let dateString = DateFormatter.reportDateFormatter.string(from: output.processingDate)
        return """
        <div class="report-header">
            <div class="report-header-brand">ReDyne</div>
            <h1>Analysis Report</h1>
            <div class="report-header-meta">
                <span>\(escapeHTML(fileName))</span>
                <span class="separator">|</span>
                <span>Generated \(escapeHTML(dateString))</span>
                <span class="separator">|</span>
                <span>ReDyne v1.0</span>
            </div>
        </div>
        """
    }

    // MARK: - Report Footer

    private static func reportFooter() -> String {
        let dateString = DateFormatter.reportDateFormatter.string(from: Date())
        return """
        <div class="report-footer">
            <p>Generated by <strong>ReDyne v1.0</strong> &mdash; Mach-O Binary Analysis Tool</p>
            <p>\(escapeHTML(dateString))</p>
        </div>
        """
    }

    // MARK: - Section 1: Binary Overview

    private static func binaryOverviewSection(output: DecompiledOutput) -> String {
        let header = output.header
        let arch: String
        if header.is64Bit {
            arch = "\(header.cpuType) (64-bit)"
        } else {
            arch = "\(header.cpuType) (32-bit)"
        }
        let platform = header.platformName ?? "Unknown"
        let fileSize = Constants.formatBytes(Int64(output.fileSize))
        let uuid = header.uuid ?? "N/A"
        let entryPoint: String
        if header.hasEntryPoint {
            entryPoint = Constants.formatAddress(header.entryPointAddress)
        } else {
            entryPoint = "N/A"
        }
        let pie = header.isPIE ? "Yes" : "No"
        let fileType = header.fileType

        return """
        <div class="section">
            <h2 class="section-title">Binary Overview</h2>
            <div class="info-grid">
                \(infoItem(label: "Architecture", value: escapeHTML(arch)))
                \(infoItem(label: "Platform", value: escapeHTML(platform)))
                \(infoItem(label: "File Size", value: escapeHTML(fileSize)))
                \(infoItem(label: "UUID", value: escapeHTML(uuid)))
                \(infoItem(label: "Entry Point", value: escapeHTML(entryPoint)))
                \(infoItem(label: "PIE", value: escapeHTML(pie)))
                \(infoItem(label: "File Type", value: escapeHTML(fileType)))
                \(infoItem(label: "Encrypted", value: header.isEncrypted ? "Yes" : "No"))
            </div>
        </div>
        """
    }

    // MARK: - Section 2: Security Assessment

    private static func securityAssessmentSection(output: DecompiledOutput) -> String {
        guard let posture = output.securityPosture as? SecurityPosture else {
            return """
            <div class="section">
                <h2 class="section-title">Security Assessment</h2>
                <p class="no-data">Security analysis data not available for this binary.</p>
            </div>
            """
        }

        var html = """
        <div class="section">
            <h2 class="section-title">Security Assessment</h2>
        """

        // Overall posture rating
        let postureColor = postureRatingColor(posture: posture)
        html += """
            <div class="posture-banner" style="border-left-color: \(postureColor);">
                <div class="posture-rating" style="color: \(postureColor);">\(escapeHTML(posture.postureSummary))</div>
                <div class="posture-detail">\(escapeHTML(posture.postureDetail))</div>
                <div class="posture-counts">
                    \(posture.criticalCount > 0 ? "<span class=\"count-badge critical\">\(posture.criticalCount) Critical</span>" : "")
                    \(posture.highCount > 0 ? "<span class=\"count-badge high\">\(posture.highCount) High</span>" : "")
                    \(posture.mediumCount > 0 ? "<span class=\"count-badge medium\">\(posture.mediumCount) Medium</span>" : "")
                    \(posture.lowCount > 0 ? "<span class=\"count-badge low\">\(posture.lowCount) Low</span>" : "")
                    \(posture.infoCount > 0 ? "<span class=\"count-badge info\">\(posture.infoCount) Info</span>" : "")
                </div>
            </div>
        """

        // Protection indicators table
        if !posture.findings.isEmpty {
            html += """
                <h3 class="subsection-title">Protection Indicators</h3>
                <div class="table-container">
                    <table>
                        <thead>
                            <tr>
                                <th>Protection</th>
                                <th>Status</th>
                                <th>Severity</th>
                                <th>Detail</th>
                            </tr>
                        </thead>
                        <tbody>
            """
            for (index, finding) in posture.findings.enumerated() {
                let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
                let statusClass = statusCSSClass(for: finding.status)
                let severityClass = severityCSSClass(for: finding.severity)
                html += """
                            <tr class="\(rowClass)">
                                <td><strong>\(escapeHTML(finding.name))</strong><br><span class="detail-text">\(escapeHTML(finding.findingDescription))</span></td>
                                <td><span class="status-badge \(statusClass)">\(escapeHTML(finding.statusString))</span></td>
                                <td><span class="severity-badge \(severityClass)">\(escapeHTML(finding.severityString))</span></td>
                                <td class="detail-text">\(escapeHTML(finding.detail))</td>
                            </tr>
                """
            }
            html += """
                        </tbody>
                    </table>
                </div>
            """
        }

        // Dangerous APIs
        if posture.hasDangerousAPIs {
            html += """
                <h3 class="subsection-title">Dangerous APIs Found</h3>
                <div class="table-container">
                    <table>
                        <thead>
                            <tr>
                                <th>Function</th>
                                <th>Risk</th>
                                <th>Severity</th>
                                <th>Imported</th>
                            </tr>
                        </thead>
                        <tbody>
            """
            for (index, api) in posture.dangerousAPIs.enumerated() {
                let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
                let severityClass = severityCSSClass(for: api.severity)
                html += """
                            <tr class="\(rowClass)">
                                <td><code>\(escapeHTML(api.functionName))</code></td>
                                <td>\(escapeHTML(api.riskDescription))</td>
                                <td><span class="severity-badge \(severityClass)">\(escapeHTML(api.severityString))</span></td>
                                <td>\(api.isImported ? "Yes" : "No")</td>
                            </tr>
                """
            }
            html += """
                        </tbody>
                    </table>
                </div>
            """
        }

        // Insecure functions
        if posture.hasInsecureFunctions {
            html += """
                <h3 class="subsection-title">Insecure Functions Found</h3>
                <div class="table-container">
                    <table>
                        <thead>
                            <tr>
                                <th>Function</th>
                                <th>Risk</th>
                                <th>Severity</th>
                            </tr>
                        </thead>
                        <tbody>
            """
            for (index, fn) in posture.insecureFunctions.enumerated() {
                let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
                let severityClass = severityCSSClass(for: fn.severity)
                html += """
                            <tr class="\(rowClass)">
                                <td><code>\(escapeHTML(fn.functionName))</code></td>
                                <td>\(escapeHTML(fn.riskDescription))</td>
                                <td><span class="severity-badge \(severityClass)">\(escapeHTML(fn.severityString))</span></td>
                            </tr>
                """
            }
            html += """
                        </tbody>
                    </table>
                </div>
            """
        }

        // Dangerous entitlements
        if posture.hasDangerousEntitlements {
            html += """
                <h3 class="subsection-title">Dangerous Entitlements</h3>
                <ul class="entitlement-list">
            """
            for entitlement in posture.dangerousEntitlements {
                html += "    <li><code>\(escapeHTML(entitlement))</code></li>\n"
            }
            html += "</ul>\n"
        }

        html += "</div>\n"
        return html
    }

    // MARK: - Section 3: Statistics Summary

    private static func statisticsSummarySection(output: DecompiledOutput) -> String {
        var rows: [(String, String)] = [
            ("Symbols (Total)", "\(output.totalSymbols)"),
            ("Symbols (Defined)", "\(output.definedSymbols)"),
            ("Symbols (Undefined)", "\(output.undefinedSymbols)"),
            ("Functions", "\(output.totalFunctions)"),
            ("Strings", "\(output.totalStrings)"),
            ("Instructions", "\(output.totalInstructions)"),
            ("Segments", "\(output.segments.count)"),
            ("Sections", "\(output.sections.count)")
        ]
        if output.totalImports > 0 {
            rows.append(("Imports", "\(output.totalImports)"))
        }
        if output.totalExports > 0 {
            rows.append(("Exports", "\(output.totalExports)"))
        }
        if output.totalObjCClasses > 0 {
            rows.append(("ObjC Classes", "\(output.totalObjCClasses)"))
        }
        if output.totalObjCMethods > 0 {
            rows.append(("ObjC Methods", "\(output.totalObjCMethods)"))
        }
        if output.totalXrefs > 0 {
            rows.append(("Cross-References", "\(output.totalXrefs)"))
        }
        if output.totalLinkedLibraries > 0 {
            rows.append(("Linked Libraries", "\(output.totalLinkedLibraries)"))
        }

        var html = """
        <div class="section">
            <h2 class="section-title">Statistics Summary</h2>
            <div class="stats-grid">
        """
        for (label, value) in rows {
            html += """
                <div class="stat-card">
                    <div class="stat-value">\(value)</div>
                    <div class="stat-label">\(escapeHTML(label))</div>
                </div>
            """
        }
        html += """
            </div>
        </div>
        """
        return html
    }

    // MARK: - Section 4: Segments & Sections

    private static func segmentsAndSectionsSection(output: DecompiledOutput) -> String {
        guard !output.segments.isEmpty else {
            return ""
        }

        var html = """
        <div class="section">
            <h2 class="section-title">Segments &amp; Sections</h2>
            <div class="table-container">
                <table>
                    <thead>
                        <tr>
                            <th>Segment</th>
                            <th>VM Address</th>
                            <th>VM Size</th>
                            <th>File Offset</th>
                            <th>File Size</th>
                            <th>Protection</th>
                        </tr>
                    </thead>
                    <tbody>
        """

        for (index, segment) in output.segments.enumerated() {
            let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
            html += """
                        <tr class="\(rowClass)">
                            <td><strong>\(escapeHTML(segment.name))</strong></td>
                            <td><code>\(Constants.formatAddress(segment.vmAddress))</code></td>
                            <td>\(Constants.formatBytes(Int64(segment.vmSize)))</td>
                            <td><code>0x\(String(format: "%llX", segment.fileOffset))</code></td>
                            <td>\(Constants.formatBytes(Int64(segment.fileSize)))</td>
                            <td><span class="prot-badge">\(escapeHTML(segment.protection))</span></td>
                        </tr>
            """

            // List sections within this segment
            let childSections = output.sections.filter { $0.segmentName == segment.name }
            for section in childSections {
                html += """
                        <tr class="\(rowClass) section-row">
                            <td class="section-indent">\(escapeHTML(section.sectionName))</td>
                            <td><code>\(Constants.formatAddress(section.address))</code></td>
                            <td>\(Constants.formatBytes(Int64(section.size)))</td>
                            <td><code>0x\(String(format: "%X", section.offset))</code></td>
                            <td></td>
                            <td></td>
                        </tr>
                """
            }
        }

        html += """
                    </tbody>
                </table>
            </div>
        </div>
        """
        return html
    }

    // MARK: - Section 5: Symbol Table

    private static func symbolTableSection(output: DecompiledOutput) -> String {
        guard !output.symbols.isEmpty else {
            return ""
        }

        let sortedSymbols = output.symbols.sortedByAddress()
        let maxSymbols = 500
        let displaySymbols = Array(sortedSymbols.prefix(maxSymbols))
        let totalCount = output.symbols.count
        let truncated = totalCount > maxSymbols

        var html = """
        <div class="section">
            <h2 class="section-title">Symbol Table\(truncated ? " (Top \(maxSymbols) of \(totalCount))" : " (\(totalCount))")</h2>
            <div class="table-container">
                <table>
                    <thead>
                        <tr>
                            <th>Address</th>
                            <th>Name</th>
                            <th>Type</th>
                            <th>Scope</th>
                        </tr>
                    </thead>
                    <tbody>
        """

        for (index, symbol) in displaySymbols.enumerated() {
            let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
            let displayName = symbol.demangledName ?? symbol.name
            html += """
                        <tr class="\(rowClass)">
                            <td><code>\(Constants.formatAddress(symbol.address))</code></td>
                            <td class="symbol-name">\(escapeHTML(displayName))</td>
                            <td><span class="type-badge">\(escapeHTML(symbol.type))</span></td>
                            <td>\(escapeHTML(symbol.scope))</td>
                        </tr>
            """
        }

        if truncated {
            html += """
                        <tr class="truncation-row">
                            <td colspan="4">... and \(totalCount - maxSymbols) more symbols not shown</td>
                        </tr>
            """
        }

        html += """
                    </tbody>
                </table>
            </div>
        </div>
        """
        return html
    }

    // MARK: - Section 6: Imports

    private static func importsSection(output: DecompiledOutput) -> String {
        guard let analysis = output.importExportAnalysis as? ImportExportAnalysis,
              !analysis.imports.isEmpty else {
            return ""
        }

        let imports = analysis.imports.sortedByName()

        var html = """
        <div class="section">
            <h2 class="section-title">Imports (\(imports.count))</h2>
            <div class="table-container">
                <table>
                    <thead>
                        <tr>
                            <th>Name</th>
                            <th>Library</th>
                        </tr>
                    </thead>
                    <tbody>
        """

        for (index, imp) in imports.enumerated() {
            let rowClass = index % 2 == 0 ? "row-even" : "row-odd"
            html += """
                        <tr class="\(rowClass)">
                            <td><code>\(escapeHTML(imp.displayName))</code>\(imp.isWeak ? " <span class=\"weak-badge\">weak</span>" : "")</td>
                            <td>\(escapeHTML(imp.libraryName))</td>
                        </tr>
            """
        }

        html += """
                    </tbody>
                </table>
            </div>
        </div>
        """
        return html
    }

    // MARK: - Section 7: Exports

